CREATE TABLE Agencies
(
    Id UNIQUEIDENTIFIER PRIMARY KEY DEFAULT NEWID(),
    Name NVARCHAR(200) NOT NULL
);

CREATE TABLE Brokers
(
    Id UNIQUEIDENTIFIER PRIMARY KEY DEFAULT NEWID(),
    AgencyId UNIQUEIDENTIFIER NOT NULL,
    Name NVARCHAR(200) NOT NULL,
    IsGoodStanding BIT NOT NULL DEFAULT 1,
    ContractStart DATETIME2 NULL,
    ContractEnd DATETIME2 NULL,
    CONSTRAINT FK_Brokers_Agencies FOREIGN KEY (AgencyId) REFERENCES Agencies(Id)
);

CREATE TABLE Users
(
    Id INT IDENTITY PRIMARY KEY,
    Oid NVARCHAR(100) NOT NULL UNIQUE,
    AgencyId UNIQUEIDENTIFIER NULL,
    BrokerId UNIQUEIDENTIFIER NULL,
    IsAgencyAdmin BIT NOT NULL DEFAULT 0,
    CONSTRAINT FK_Users_Agencies FOREIGN KEY (AgencyId) REFERENCES Agencies(Id),
    CONSTRAINT FK_Users_Brokers FOREIGN KEY (BrokerId) REFERENCES Brokers(Id)
);

